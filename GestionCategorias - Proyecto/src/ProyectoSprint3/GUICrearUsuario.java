    /**
     * @param args the command line arguments
     */
    private void createUser() {

        try {
            bbdd conexion = new bbdd();
            String user = fieldName.getText();
            String pass = new String(fieldPass.getPassword());
            String passCheck = new String(fieldPassCheck.getPassword());

            if (pass.length() < 3 || user.length() < 3) {
                JOptionPane.showMessageDialog(null, "El usuario "
                        + "o contrase침a tiene que tener m치s de 2 caracteres\n",
                        "Error", JOptionPane.WARNING_MESSAGE);
                fieldName.requestFocus();
                fieldPass.setText("");
                fieldPassCheck.setText("");
            } else {
                if (pass.equals(passCheck)) {
                    byte check = conexion.datosCreate(user);
                    if (check >= 1) {
                        JOptionPane.showMessageDialog(null, "Este usuario ya "
                                + "est치 siendo usado\n",
                                "Error", JOptionPane.WARNING_MESSAGE);
                        fieldName.setText("");
                        fieldPass.setText("");
                        fieldPassCheck.setText("");
                        fieldName.requestFocus();
                    } else if (check == 0) {
                        conexion.crearUsuario(user, pass);
                        JOptionPane.showMessageDialog(null, "El usuario "
                                + " ha sido creado. Te redirigimos a la "
                                + "pantalla de login:\n",
                                "Registro satisfactorio",
                                JOptionPane.INFORMATION_MESSAGE);
                        this.setVisible(false);
                        new GUILog().setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Las contrase침as "
                            + "introducidas son diferentes:\n",
                            "Error", JOptionPane.ERROR_MESSAGE);
                    fieldPass.setText("");
                    fieldPassCheck.setText("");
                    fieldPass.requestFocus();
                }
            }
            conexion.close();
        } catch (Exception e) {
        }

    }
